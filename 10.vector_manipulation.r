#Two vectors of the same length can be added, subtracted, multiplied or divided 
#giving the result as a vector output

#Create two vectors
v1 <- c(3,8,4,5,0,11)
v2 <- c(4,11,0,8,1,2)

#Add
add.result <- v1 + v2
print(add.result)

#Subtract 
sub.result <- v1 - v2
print(sub.result)

#Multiply
multi.result <- v1 * v2
print(multi.result)

#Divide
div.result <- v1/v2
print(div.result)

